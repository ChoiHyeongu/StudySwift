import UIKit

class Triple<A: Any, B: Any, C: Any> {
  var first: A
  var second: B
  var third: C

  init(_ first: A, _ second: B, _ third: C) {
    self.first = first
    self.second = second
    self.third = third
  }

  func toList() -> [Any] {
    return [first, second, third]
  }

  func toString() -> String {
    return "(\(first), \(second), \(third))"
  }
}

let triple = Triple(1, "A", "Choi")

print(triple.toList())
print(triple.toString())
